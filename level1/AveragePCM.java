class AveragePCM {

    public static void main(String[] args) {

        String studentName = "Sam";

        int maths = 94;
        int physics = 95;
        int chemistry = 96;

        double average = (maths + physics + chemistry) / 3.0;

        System.out.println(studentName + "'s average mark in PCM is " + average);

    }
}
